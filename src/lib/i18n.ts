export function translate(language: AppLanguage, key: string, params?: TranslationParams): string {
  const template = translations[language][key] ?? translations.en[key] ?? key

  if (!params) {
    return template
  }

  return Object.entries(params).reduce((result, [name, value]) => {
    return result.split(`{${name}}`).join(String(value))
  }, template)
}

export function useTranslation() {
  const language = useSettingsStore((state) => state.settings?.language ?? 'no')

  return {
    language,
    t: (key: string, params?: TranslationParams) => translate(language, key, params),
  }
}
